/**
 * 根据题材类型获取对应的 system prompt 追加内容
 */
export function getGenrePrompt(genre: string | null | undefined): string {
  if (!genre) return GENRE_PROMPTS['默认']

  // 模糊匹配
  const normalized = genre.trim()
  for (const [key, prompt] of Object.entries(GENRE_PROMPTS)) {
    if (normalized.includes(key)) return prompt
  }

  // 反向匹配（key 包含 genre）
  for (const [key, prompt] of Object.entries(GENRE_PROMPTS)) {
    if (key.includes(normalized) && key !== '默认') return prompt
  }

  return GENRE_PROMPTS['默认']
}

/**
 * 检测文本是否符合题材爽点（简单规则检查）
 * 返回发现的问题列表
 */
export function checkGenreSatisfaction(genre: string | null | undefined, content: string): string[] {
  const issues: string[] = []
  const text = content.replace(/\s/g, '')

  if (!genre) return issues

  // 种田文检查
  if (genre.includes('种田') || genre.includes('经营') || genre.includes('基建')) {
    // 检查是否有具体产出
    const hasOutput = /收获|建成|利润|收益|产出|赚了|收了|得了/.test(text)
    if (!hasOutput) issues.push('缺少具体产出（收获/建成/利润等）')

    // 检查是否有量化
    const hasQuantify = /\d+亩|\d+两|\d+斤|\d+文|\d+两银子|\d+石/.test(text)
    if (!hasQuantify) issues.push('缺少量化描述（亩/两/斤/石等）')

    // 检查是否有古代现代混杂
    const ancientWords = /马贼|村长|锄头|羊皮纸|旱灾/.test(text)
    const modernWords = /生态学家|专家|先进知识|技术|科学/.test(text)
    if (ancientWords && modernWords) {
      issues.push('疑似古代/现代背景混杂（出现"生态学家""专家"等现代词汇）')
    }
  }

  // 都市异能检查
  if (genre.includes('都市') || genre.includes('异能') || genre.includes('副本')) {
    // 检查是否有规则说明
    const hasRule = /规则|条件|机制|限制|奖励|代价/.test(text)
    if (!hasRule) issues.push('缺少规则/机制/代价的说明')

    // 检查是否有代价
    const hasCost = /透支|侵蚀|副作用|代价|虚弱|头痛|昏迷/.test(text)
    if (!hasCost) issues.push('能力使用缺少代价')
  }

  return issues
}
